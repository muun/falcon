    func testSimmetricDerivation() throws {
        
        let privateKey = WalletPrivateKey.fromBase58(
            "xprv9s21ZrQH143K31xYSDQpPDxsXRTUcvj2iNHm5NUtrGiGG5e2DtALGdso3pGz6ssrdK4PFmM8NSpSBHNqPqm55Qn3LqFtT2emdEXVYsCzC2U",
            on: "m")
        let publicKey = privateKey.walletPublicKey()
        
        assertKey(
            publicKey,
            "xpub661MyMwAqRbcFW31YEwpkMuc5THy2PSt5bDMsktWQcFF8syAmRUapSCGu8ED9W6oDMSgv6Zz8idoc4a6mr8BDzTJY47LJhkJ8UB7WEGuduB")
        
        let firstChain = try privateKey.derive(to: "m/0")
        assertKey(
            firstChain,
            "xprv9vHkqa6EV4sPZHYqZznhT2NPtPCjKuDKGY38FBWLvgaDx45zo9WQRUT3dKYnjwih2yJD9mkrocEZXo1ex8G81dwSM1fwqWpWkeS3v86pgKt")
        
        assertKey(
            firstChain.walletPublicKey(),
            "xpub69H7F5d8KSRgmmdJg2KhpAK8SR3DjMwAdkxj3ZuxV27CprR9LgpeyGmXUbC6wb7ERfvrnKZjXoUmmDznezpbZb7ap6r1D3tgFxHmwMkQTPH")
        
        XCTAssertEqual(firstChain.walletPublicKey(), try publicKey.derive(to: "m/0"))
        
        let secondChain = try privateKey.derive(to: "m/0/2147483647'/1")
        assertKey(
            secondChain,
            "xprv9zFnWC6h2cLgpmSA46vutJzBcfJ8yaJGg8cX1e5StJh45BBciYTRXSd25UEPVuesF9yog62tGAQtHjXajPPdbRCHuWS6T8XA2ECKADdw4Ef")
        
        assertKey(
            secondChain.walletPublicKey(),
            "xpub6DF8uhdarytz3FWdA8TvFSvvAh8dP3283MY7p2V4SeE2wyWmG5mg5EwVvmdMVCQcoNJxGoWaU9DCWh89LojfZ537wTfunKau47EL2dhHKon")
        
        XCTAssertEqual(
            secondChain.walletPublicKey(),
            try privateKey.derive(to: "m/0/2147483647'").walletPublicKey().derive(at: 1))
        
        let thirdChain = try privateKey.derive(to: "m/0/2147483647'/1/2147483646'")
        assertKey(
            thirdChain,
            "xprvA1RpRA33e1JQ7ifknakTFpgNXPmW2YvmhqLQYMmrj4xJXXWYpDPS3xz7iAxn8L39njGVyuoseXzU6rcxFLJ8HFsTjSyQbLYnMpCqE2VbFWc")
        
        assertKey(
            thirdChain.walletPublicKey(),
            "xpub6ERApfZwUNrhLCkDtcHTcxd75RbzS1ed54G1LkBUHQVHQKqhMkhgbmJbZRkrgZw4koxb5JaHWkY4ALHY2grBGRjaDMzQLcgJvLJuZZvRcEL")
    }
    
    private func assertKey(_ key: WalletPrivateKey, _ encoded: String) {
        XCTAssertEqual(key.toBase58(), encoded)
    }
    
    private func assertKey(_ key: WalletPublicKey, _ encoded: String) {
        XCTAssertEqual(key.toBase58(), encoded)
    }
    
}
